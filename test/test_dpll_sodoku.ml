let solve_and_print puzzle =
  let result = puzzle
    |> load_sodoku
    |> sodoku_to_prop_expr
    |> sat_solve in
  match result with
    | Some interp -> interp |> interp_to_sodoku |> export_sodoku |> print_endline
    | None -> print_endline "NO SOLUTION";;

